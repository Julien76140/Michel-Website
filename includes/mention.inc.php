<h1>Mentions propres au site particulier </h1>

<p style="padding: 20px;">Le particulier qui crée un site internet n'est pas obligé de révéler son identité. Il doit
    cependant communiquer à l'hébergeur de son site les éléments qui permettront de l'identifier en cas de besoin. Ces
    éléments d'identification sont couverts par le secret professionnel. Mais l'hébergeur du site internet a
    l'obligation de les dévoiler dans le cadre d'une procédure judiciaire.

    Si le particulier choisit de garder l'anonymat, les seules mentions obligatoires à retrouver sur son site internet
    sont : nom, prénom, adresse et numéro de téléphone de l'hébergeur de son site internet.
    Si le particulier ne souhaite pas garder l'anonymat, il doit indiquer les mentions légales le concernant et celles
    de l'hébergeur de son site internet.</p>
<h1>Mentions relatives à l'utilisation de cookies</h1>

<p style="padding: 20px;">Un cookie est un petit fichier informatique qui permet d'analyser le comportement des usagers
    lors de la visite d'un site internet ou de l'utilisation d'un logiciel ou d'une application mobile.

    Les éditeurs de sites ou d'applications qui utilisent des cookies doivent :

    informer les internautes de la finalité des cookies,
    obtenir leur consentement,
    fournir aux internautes un moyen de les refuser.
    La durée de validité du consentement donné dans ce cadre est de 13 mois maximum.</p>
<h1>Mentions relatives à l'utilisation de données personnelles</h1>
<p style="padding: 20px;">Les sites qui utilisent des données personnelles doivent obligatoires mentionner les
    informations suivantes :

    Coordonnées du délégué à la protection des données (DPO ou DPD) de l'organisme, s'il a été désigné, ou d'un point de
    contact sur les questions de protection des données personnelles
    Finalité poursuivie par le traitement auquel les données sont destinées
    Caractère obligatoire ou facultatif des réponses et conséquences éventuelles à l'égard de l'internaute d'un défaut
    de réponse
    Destinataires ou catégories de destinataires des données
    Droits d'opposition, d'interrogation, d'accès et de rectification
    Au besoin, transferts de données à caractère personnel envisagés à destination d'un État n'appartenant pas à l'Union
    européenne
    Base juridique du traitement de données (c'est-à-dire ce qui autorise légalement le traitement : il peut s'agir du
    consentement des personnes concernées, du respect d'une obligation prévue par un texte, de l'exécution d'un contrat
    notamment)
    Droit d'introduire une réclamation auprès de la Cnil</p>
