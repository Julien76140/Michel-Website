                    installation laser. D’autres documents analysent le concept de photovoile : une voile qui capterait
                    le faisceau laser et convertirait son énergie en mouvement. Certains chercheurs se demandent si une
                    telle voile se désagrégerait sous l’effet de la chaleur ou d’une accélération abrupte (environ
                    60’000 fois la force de gravité exercée à la surface de la Terre). Il y a également un risque que la
                    voile dirige un StarChip complètement en dehors de la trajectoire initiale.
