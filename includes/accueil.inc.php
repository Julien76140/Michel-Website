    <h3>Breakthough strashot</h3>

    <p>Le projet Breakthrough Starshot, lancé début 2016, a pour objectif d'envoyer des milliers de sondes spatiales (d'environ 1 gramme), équipées de voiles solaires, vers Alpha du Centaure le système stellaire le plus proche du système solaire (4 année lumière de nous).Allié à la puissance du laser terrestre utilisé pour les propulser jusqu'à 100 gigawatts permettrait à ces dernières d'atteindre 20 % de la vitesse de la lumière dans le vide et ainsi de pouvoir nous retourner des images des exoplanètes potentielles d'Alpha du Centaure, notamment de Proxima Centauri b, en 40 à 50 ans. Le programme est piloté par Iouri Milner, Mark Zuckerberg et anciennement Stephen Hawking (Malheureusement décédé en 2018). Le comité de direction est constitué de vingt-quatre scientifiques de renom, sous la direction de Pete Worden, ancien directeur du centre de recherche Ames de la NASA.</p>
